package com.kg.seeot.product.dto;

public class ProductManageDTO {
	private int productNo, productStack, productSize;
	private String productColor;
	
	
	public int getProductNo() {
		return productNo;
	}
	public void setProductNo(int productNo) {
		this.productNo = productNo;
	}
	public int getProductStack() {
		return productStack;
	}
	public void setProductStack(int productStack) {
		this.productStack = productStack;
	}
	public int getProductSize() {
		return productSize;
	}
	public void setProductSize(int productSize) {
		this.productSize = productSize;
	}
	public String getProductColor() {
		return productColor;
	}
	public void setProductColor(String productColor) {
		this.productColor = productColor;
	}
	
}